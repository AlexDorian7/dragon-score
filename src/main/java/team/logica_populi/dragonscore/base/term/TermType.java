package team.logica_populi.dragonscore.base.term;

/**
 * Term Type enum
 * Defines what types of terms exist
 */
public enum TermType {
    /**
     * A noun
     */
    NOUN,

    /**
     * A proper noun
     */
    PRO_NOUN,

    /**
     * A verb
     */
    VERB,

    /**
     * An adjective
     */
    ADJ,

    /**
     * A singular article
     */
    ART_S,

    /**
     * A plural article
     */
    ART_P,
}